package com.biz.book.domain;


public class BookVO {

	
	private String b_isbn;
	private String b_title;
	private String b_writer;
	private String b_comp;
	private String b_trans;
	private int b_price;
	private int b_page;
	private String b_date;
	
	public String getB_isbn() {
		return b_isbn;
	}
	public void setB_isbn(String b_isbn) {
		this.b_isbn = b_isbn;
	}
	public String getB_title() {
		return b_title;
	}
	public void setB_title(String b_title) {
		this.b_title = b_title;
	}
	public String getB_writer() {
		return b_writer;
	}
	public void setB_writer(String b_writer) {
		this.b_writer = b_writer;
	}
	public String getB_comp() {
		return b_comp;
	}
	public void setB_comp(String b_comp) {
		this.b_comp = b_comp;
	}
	public String getB_trans() {
		return b_trans;
	}
	public void setB_trans(String b_trans) {
		this.b_trans = b_trans;
	}
	public int getB_price() {
		return b_price;
	}
	public void setB_price(int b_price) {
		this.b_price = b_price;
	}
	public int getB_page() {
		return b_page;
	}
	public void setB_page(int b_page) {
		this.b_page = b_page;
	}
	public String getB_date() {
		return b_date;
	}
	public void setB_date(String b_date) {
		this.b_date = b_date;
	}
		
}
